    private void btnMasukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMasukActionPerformed

        String namaPengguna = txtNamaPengguna.getText().trim();
        String kataSandi = new String(pwdKataSandi.getPassword());

        if (namaPengguna.isEmpty() || kataSandi.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Username dan password wajib diisi.",
                    "Peringatan",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            boolean berhasil = penggunaPengelola.validasiLogin(namaPengguna, kataSandi);

            if (berhasil) {
                // ambil objek Pengguna yang berhasil login
                Pengguna pengguna = penggunaPengelola.cariPenggunaByUsername(namaPengguna);
                // nama method di atas kamu sesuaikan ya

                JOptionPane.showMessageDialog(this,
                        "Login berhasil. Selamat datang, " + namaPengguna + "!",
                        "Informasi", JOptionPane.INFORMATION_MESSAGE);

                this.dispose();

                // kirim objek pengguna ke MenuUtamaFrame
                new MenuUtamaFrame(pengguna).setVisible(true);

            } else {
                JOptionPane.showMessageDialog(this,
                        "Username atau password salah.",
                        "Gagal Login", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Terjadi kesalahan: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnMasukActionPerformed

